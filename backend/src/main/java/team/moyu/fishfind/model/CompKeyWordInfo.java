package team.moyu.fishfind.model;

public class CompKeyWordInfo {
  private String compKeyWord;
  private Long ka;
  private double compScore;

  public String getCompKeyWord() {
    return compKeyWord;
  }

  public void setCompKeyWord(String compKeyWord) {
    this.compKeyWord = compKeyWord;
  }

  public Long getKa() {
    return ka;
  }

  public void setKa(Long ka) {
    this.ka = ka;
  }

  public double getCompScore() {
    return compScore;
  }

  public void setCompScore(double compScore) {
    this.compScore = compScore;
  }
}
